with resolved as (
  select v.*, coalesce(v.manual_material_id, m.id) as material_id
  from verify_k55_std v
  left join public.panol_materiales m
    on v.manual_material_id is null and lower(btrim(m.descripcion)) = lower(btrim(v.descripcion))
), dedup as (
  select distinct on (excel_row) *
  from resolved
  order by excel_row, material_id nulls last
), checked as (
  select d.*, mm.cantidad as k55_cantidad
  from dedup d
  left join public.panol_material_modelo mm
    on mm.material_id = d.material_id and mm.modelo='55' and mm.variante='standard'
)
select
  count(*)::int as std_excel,
  count(*) filter (where material_id is null)::int as sin_material,
  count(*) filter (where material_id is not null and k55_cantidad is null)::int as sin_k55,
  count(*) filter (where material_id is not null and k55_cantidad is not null)::int as cubiertas
from checked;
